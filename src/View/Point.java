package View;

public class Point 
{
	private Integer posX;
	private Integer posY;
	
	void setPosX(Integer p_posX)
	{
		this.posX = p_posX;
	}
	
	void setPosY(Integer p_posY)
	{
		this.posY = p_posY;
	}
	
	Integer getPosX()
	{
		return this.posX;
	}
	
	Integer getPosY()
	{
		return this.posY;
	}
}
